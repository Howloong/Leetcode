    class MyCircularDeque {

        public int[] queue;
        public int front;
        public int rear;
        public int size;
        public int length;

        public MyCircularDeque(int k) {
            queue = new int[k];
            size = k;
            front = 0;
            rear = 0;
            length = 0;
        }

        public boolean insertFront(int value) {
            if (isFull()) {
                return false;
            }
            length++;
            front--;
            if (front == -1) {
                front = size - 1;
            }
            queue[front] = value;
            return true;
        }

        public boolean insertLast(int value) {
            if (isFull()) {
                return false;
            }
            length++;
            queue[rear] = value;
            rear++;
            if (rear == size) {
                rear = 0;
            }
            return true;
        }

        public boolean deleteFront() {
            if (isEmpty()) {
                return false;
            }
            length--;
            front++;
            if (front == size) {
                front = 0;
            }
            return true;
        }

        public boolean deleteLast() {
            if (isEmpty()) {
                return false;
            }
            length--;
            rear--;
            if (rear == -1) {
                rear = size - 1;
            }
            return true;
        }

        public int getFront() {
            return isEmpty() ? -1 : queue[front];
        }

        public int getRear() {
            return isEmpty() ? -1 : queue[rear - 1 == -1 ? size - 1 : rear - 1];
        }

        public boolean isEmpty() {
            return length == 0;
        }

        public boolean isFull() {
            return length == size;
        }
    }

