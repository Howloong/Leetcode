    class Solution {
        public boolean isValidSudoku(char[][] board) {
            int n = board.length;
            for (int i = 0; i < n; i++) {
                int[] c1 = new int[9];
                int[] c2 = new int[9];
                for (int j = 0; j < n; j++) {
                    if (board[i][j] != '.') {
                        if (c1[board[i][j] - '0' - 1] == 1) {
                            return false;
                        }
                        c1[board[i][j] - '0' - 1]++;
                    }
                    if (board[j][i] != '.' ) {
                        if (c2[board[j][i] - '0' - 1] == 1)
                            return false;
                        c2[board[j][i] - '0' - 1]++;
                    }
                    int startRow = 3 * (i / 3);
                    int startCol = 3 * (j / 3);
                    for (int k = startRow; k < startRow + 3; k++) {
                        for (int l = startCol; l < startCol + 3; l++) {
                            if (board[i][j]!='.'&&board[i][j] == board[k][l] && i != k && j != l) {
                                return false;
                            }
                        }
                    }
                }
            }
            return true;
        }
    }
